_NEUTRAL = {"cyclicality": "중립", "capital_intensity": "중립"}


def classify_industry(induty_code: Optional[str]) -> dict:
    """induty_code(KSIC, 보통 5자리 문자열)를 앞 2자리(중분류) 기준으로 분류한다.
    매칭 안 되면 두 축 모두 '중립'(보정 없음)을 반환 — 안전한 기본값."""
